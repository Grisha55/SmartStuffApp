import UIKit

extension UIColor {
    
    static var blackBlue: UIColor {
        return UIColor(red: 25/255, green: 33/255, blue: 44/255, alpha: 1)
    }
    
    static var grayBlue: UIColor {
        return UIColor(red: 25/255, green: 33/255, blue: 44/255, alpha: 0.5)
    }
    
    static var darkBlue: UIColor {
        return UIColor(red: 35/255, green: 33/255, blue: 152/255, alpha: 1)
    }
    
    static var customPurple: UIColor {
        return UIColor(red: 0.533, green: 0.373, blue: 0.973, alpha: 1)
    }
    
}
